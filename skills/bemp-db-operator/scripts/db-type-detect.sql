-- ==========================================================================
-- 数据库类型自动检测脚本
-- 用途：当defaultDbType为auto时，通过探测MCP工具可用性自动判断数据库类型
-- 使用方式：按以下步骤逐个探测，首次成功即确定数据库类型
-- ==========================================================================

-- ==========================================================================
-- 检测步骤1：探测Oracle MCP可用性
-- 调用方式：mcp_oracle-mcp_list_schemas()
-- 成功 → 数据库类型为Oracle，跳过后续检测
-- 失败 → 继续步骤2
-- ==========================================================================

-- Oracle连接验证查询（通过Oracle MCP执行）
-- 成功表示Oracle MCP可用，数据库类型判定为oracle
SELECT USER FROM DUAL;

-- Oracle Schema列表查询（进一步验证）
-- 成功表示Oracle连接完整可用
-- mcp_oracle-mcp_list_schemas() → 返回Schema列表

-- Oracle表列表查询（确认目标Schema可访问）
-- mcp_oracle-mcp_list_tables(schema="BEMP_HNNX") → 返回表列表

-- ==========================================================================
-- 检测步骤2：探测MySQL MCP可用性
-- 调用方式：mcp_MySQL_execute_sql(query="SELECT 1 AS db_type_probe")
-- 成功 → 数据库类型为MySQL
-- 失败 → 两种MCP均不可用，报错终止
-- ==========================================================================

-- MySQL连接验证查询（通过MySQL MCP执行）
-- 成功表示MySQL MCP可用，数据库类型判定为mysql
SELECT 1 AS db_type_probe;

-- MySQL数据库列表查询（进一步验证）
SHOW DATABASES;

-- MySQL表列表查询（确认目标数据库可访问）
SHOW TABLES;

-- ==========================================================================
-- 检测步骤3：双数据库同时可用时的优先级判定
-- 参照db-config.json中autoDetection.detectionPriority配置
-- 默认优先级：["oracle", "mysql"] → 优先使用Oracle
-- 可修改为：["mysql", "oracle"] → 优先使用MySQL
-- ==========================================================================

-- 优先级判定逻辑：
-- 1. 按detectionPriority顺序探测
-- 2. 首个可用的MCP对应的数据库类型即为检测结果
-- 3. 如配置了fallbackDbType，当优先级列表中的均不可用时使用fallback

-- ==========================================================================
-- 检测结果缓存
-- 检测成功后缓存结果，避免重复检测
-- 缓存时长：cacheDurationSeconds（默认3600秒=1小时）
-- 缓存失效条件：
--   1. 缓存超时
--   2. MCP工具调用失败（连接断开）
--   3. 手动清除缓存（修改defaultDbType为非auto值再改回auto）
-- ==========================================================================

-- ==========================================================================
-- 检测结果记录格式
-- ==========================================================================
-- 检测时间：{timestamp}
-- 检测结果：{oracle|mysql}
-- 检测方式：auto_detection
-- Oracle MCP状态：{available|unavailable}
-- MySQL MCP状态：{available|unavailable}
-- 使用优先级：{detectionPriority值}
-- 缓存有效期至：{timestamp + cacheDurationSeconds}
-- ==========================================================================

-- ==========================================================================
-- SQL执行前预检查脚本
-- 用途：在执行增量SQL前，检查目标表结构和数据状态
-- 使用方式：根据实际变更内容替换变量后，通过 Oracle MCP 执行
-- ==========================================================================

-- ==========================================================================
-- 检查1：目标表是否存在
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
SELECT TABLE_NAME, NUM_ROWS FROM USER_TABLES WHERE TABLE_NAME = UPPER('{TABLE_NAME}');

-- ==========================================================================
-- 检查2：目标表结构（字段列表）
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
-- 调用：mcp_oracle-mcp_describe_table(table="{TABLE_NAME}", schema="BEMP_HNNX")

-- ==========================================================================
-- 检查3：目标表字段详情
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
SELECT COLUMN_ID, COLUMN_NAME, DATA_TYPE, DATA_LENGTH, DATA_PRECISION, DATA_SCALE, NULLABLE, DATA_DEFAULT
FROM USER_TAB_COLUMNS
WHERE TABLE_NAME = UPPER('{TABLE_NAME}')
ORDER BY COLUMN_ID;

-- ==========================================================================
-- 检查4：预估DELETE影响行数
-- 替换 {TABLE_NAME} 和 {CONDITION} 为实际值
-- 用途：将DELETE的WHERE条件转为SELECT COUNT(*)预估影响范围
-- ==========================================================================
SELECT COUNT(*) AS AFFECT_ROWS FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 检查5：预估UPDATE影响行数
-- 替换 {TABLE_NAME} 和 {CONDITION} 为实际值
-- ==========================================================================
SELECT COUNT(*) AS AFFECT_ROWS FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 检查6：执行前数据快照（用于复核和回退）
-- 替换 {TABLE_NAME}、{COLUMNS}、{CONDITION} 为实际值
-- ==========================================================================
SELECT {COLUMNS} FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 检查7：菜单ID冲突检查
-- 替换 {MENU_ID_LIST} 为新增菜单ID列表
-- 用途：确认新增的菜单ID不会与已有ID冲突
-- ==========================================================================
SELECT ID, AUTH_NAME FROM TM_AUTHORITY WHERE ID IN ({MENU_ID_LIST});

-- ==========================================================================
-- 检查8：业务参数KEY冲突检查
-- 替换 {PARAM_KEY_LIST} 为新增参数KEY列表
-- 用途：确认新增的参数KEY不会与已有KEY冲突
-- ==========================================================================
SELECT PARAM_KEY, PARAM_NAME, PARAM_VALUE FROM TM_BUSINESS_PARAMETER WHERE PARAM_KEY IN ({PARAM_KEY_LIST});

-- ==========================================================================
-- 预检查结果判定标准：
-- 检查1：表存在 → 可继续 / 表不存在 → 需先执行DDL
-- 检查4-5：影响行数 < affectRowWarningThreshold → 正常
--          影响行数 >= affectRowWarningThreshold → 警告确认
--          影响行数 >= affectRowAbortThreshold → 中止执行
-- 检查7-8：无冲突 → 可继续 / 有冲突 → 需修改ID或KEY
-- ==========================================================================

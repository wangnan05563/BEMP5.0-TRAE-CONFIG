-- ==========================================================================
-- MySQL SQL执行后验证脚本
-- 用途：在执行增量SQL后，验证数据变更结果是否符合预期
-- 使用方式：根据实际变更内容替换变量后，通过 MySQL MCP 执行
-- ==========================================================================

-- ==========================================================================
-- 验证1：DDL变更 - 表结构验证
-- 替换 {TABLE_NAME} 为实际表名
-- 调用：mcp_MySQL_execute_sql(query="DESCRIBE {TABLE_NAME}")
-- 预期：返回的表结构包含新增的字段
-- ==========================================================================

-- ==========================================================================
-- 验证2：DDL变更 - 索引验证
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
SHOW INDEX FROM {TABLE_NAME};

-- ==========================================================================
-- 验证3：DDL变更 - AUTO_INCREMENT验证（MySQL特有，替代Oracle序列验证）
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
SELECT AUTO_INCREMENT
FROM INFORMATION_SCHEMA.TABLES
WHERE TABLE_SCHEMA = DATABASE()
  AND TABLE_NAME = '{TABLE_NAME}';

-- ==========================================================================
-- 验证4：DML变更 - INSERT数据验证
-- 替换 {TABLE_NAME}、{COLUMNS}、{CONDITION} 为实际值
-- ==========================================================================
SELECT {COLUMNS} FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 验证5：DML变更 - 菜单数据验证
-- 替换 {MENU_ID_LIST} 为新增菜单ID列表
-- ==========================================================================
SELECT ID, AUTH_NAME, AUTH_TYPE, PARENT_ID, URL, IS_LEAF, STATUS
FROM TM_AUTHORITY
WHERE ID IN ({MENU_ID_LIST})
ORDER BY ID;

-- ==========================================================================
-- 验证6：DML变更 - 业务参数验证
-- 替换 {PARAM_KEY_LIST} 为新增参数KEY列表
-- ==========================================================================
SELECT PARAM_KEY, PARAM_NAME, PARAM_VALUE, PARAM_TYPE
FROM TM_BUSINESS_PARAMETER
WHERE PARAM_KEY IN ({PARAM_KEY_LIST});

-- ==========================================================================
-- 验证7：DML变更 - 删除数据验证
-- 替换 {TABLE_NAME} 和 {CONDITION} 为实际值
-- 预期：查询结果为0行
-- ==========================================================================
SELECT COUNT(*) AS REMAINING_ROWS FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 验证8：数据一致性验证 - 比对执行前后记录数
-- ==========================================================================
SELECT
    '{TABLE_NAME}' AS TABLE_NAME,
    COUNT(*) AS TOTAL_ROWS
FROM {TABLE_NAME};

-- ==========================================================================
-- 验证9：菜单树完整性验证
-- 替换 {PARENT_ID} 为父菜单ID
-- 验证新增菜单的父子关系是否正确
-- ==========================================================================
SELECT
    P.ID AS PARENT_ID,
    P.AUTH_NAME AS PARENT_NAME,
    C.ID AS CHILD_ID,
    C.AUTH_NAME AS CHILD_NAME,
    C.URL AS CHILD_URL
FROM TM_AUTHORITY P
LEFT JOIN TM_AUTHORITY C ON C.PARENT_ID = P.ID
WHERE P.ID = {PARENT_ID}
ORDER BY C.SORT_NO;

-- ==========================================================================
-- 验证10：表引擎和字符集验证（MySQL特有）
-- 替换 {TABLE_NAME} 为实际表名
-- ==========================================================================
SELECT TABLE_NAME, ENGINE, TABLE_COLLATION, TABLE_ROWS
FROM INFORMATION_SCHEMA.TABLES
WHERE TABLE_SCHEMA = DATABASE()
  AND TABLE_NAME = '{TABLE_NAME}';

-- ==========================================================================
-- 验证结果判定标准：
-- 验证1-3：结构符合预期 → 通过
-- 验证4-6：数据内容与预期一致 → 通过
-- 验证7：剩余行数为0 → 删除成功
-- 验证8：记录数变化符合预期 → 通过
-- 验证9：父子关系正确 → 通过
-- 验证10：引擎和字符集符合预期 → 通过
-- 全部通过 → 执行成功
-- 任一不通过 → 需排查或回退
-- ==========================================================================

-- ==========================================================================
-- MySQL 回退脚本模板
-- 用途：当SQL执行失败或复核不通过时，根据执行前快照生成回退SQL
-- 使用方式：根据实际变更内容填充回退语句，通过 MySQL MCP 的 execute_sql 执行
-- ==========================================================================

-- ==========================================================================
-- 回退策略说明
-- ==========================================================================
-- statement级：仅回退失败的SQL语句
-- script级：回退整个脚本内的所有变更（推荐）
-- full级：恢复到执行前的完整数据快照
--
-- MySQL 特有优势：
-- - 可通过 mcp_MySQL_execute_sql 直接执行回退SQL
-- - 支持事务回退：START TRANSACTION + ROLLBACK
-- - 支持 DROP TABLE IF EXISTS / DROP INDEX IF EXISTS 安全语法
-- ==========================================================================

-- ==========================================================================
-- 事务回退模式（推荐用于关联DML操作）
-- 如执行时使用了 START TRANSACTION，可直接 ROLLBACK 整体回退
-- ==========================================================================
-- ROLLBACK;

-- ==========================================================================
-- DML回退 - INSERT的反操作
-- 原操作：INSERT INTO {TABLE_NAME} VALUES (...)
-- 回退操作：DELETE
-- ==========================================================================
DELETE FROM {TABLE_NAME} WHERE ID IN ({INSERTED_ID_LIST});

-- ==========================================================================
-- DML回退 - DELETE的反操作
-- 原操作：DELETE FROM {TABLE_NAME} WHERE {CONDITION}
-- 回退操作：INSERT（使用执行前快照数据）
-- ==========================================================================
INSERT INTO {TABLE_NAME} ({COLUMN_LIST}) VALUES ({SNAPSHOT_VALUES});

-- ==========================================================================
-- DML回退 - UPDATE的反操作
-- 原操作：UPDATE {TABLE_NAME} SET {FIELD}={NEW_VALUE} WHERE {CONDITION}
-- 回退操作：UPDATE（使用执行前快照数据）
-- ==========================================================================
UPDATE {TABLE_NAME} SET {FIELD} = {OLD_VALUE} WHERE ID = {ID};

-- ==========================================================================
-- DDL回退 - CREATE TABLE的反操作
-- 原操作：CREATE TABLE {TABLE_NAME} (...)
-- 回退操作：DROP TABLE（MySQL使用 IF EXISTS 安全语法）
-- ==========================================================================
DROP TABLE IF EXISTS {TABLE_NAME};

-- ==========================================================================
-- DDL回退 - ALTER TABLE ADD COLUMN的反操作
-- 原操作：ALTER TABLE {TABLE_NAME} ADD COLUMN {COLUMN_NAME} {DATA_TYPE}
-- 回退操作：ALTER TABLE DROP COLUMN
-- ==========================================================================
ALTER TABLE {TABLE_NAME} DROP COLUMN {COLUMN_NAME};

-- ==========================================================================
-- DDL回退 - CREATE INDEX的反操作
-- 原操作：CREATE INDEX {INDEX_NAME} ON {TABLE_NAME} ({COLUMNS})
-- 回退操作：DROP INDEX（MySQL需指定表名）
-- ==========================================================================
DROP INDEX {INDEX_NAME} ON {TABLE_NAME};

-- ==========================================================================
-- 菜单数据回退示例
-- 原操作：新增菜单ID为 {MENU_ID_LIST} 的菜单
-- ==========================================================================
DELETE FROM TM_PEND_ITEM WHERE AUTH_ID IN ({MENU_ID_LIST});
DELETE FROM TM_AUTHORITY WHERE ID IN ({MENU_ID_LIST});

-- ==========================================================================
-- 业务参数回退示例
-- 原操作：新增PARAM_KEY为 {PARAM_KEY_LIST} 的参数
-- ==========================================================================
DELETE FROM TM_BUSINESS_PARAMETER WHERE PARAM_KEY IN ({PARAM_KEY_LIST});

-- ==========================================================================
-- 回退验证
-- 执行回退后，需验证数据已恢复到执行前状态
-- ==========================================================================
SELECT COUNT(*) AS REMAINING FROM {TABLE_NAME} WHERE {CONDITION};

-- ==========================================================================
-- 回退脚本使用注意事项：
-- 1. 回退脚本必须在执行前生成，与执行脚本配套
-- 2. 回退顺序与执行顺序相反（后执行的先回退）
-- 3. 回退后必须验证数据一致性
-- 4. 回退脚本同样遵循"先删除后新增"策略
-- 5. MySQL MCP可直接执行回退SQL，无需额外工具
-- 6. 推荐使用 IF EXISTS 语法避免回退时报错
-- 7. MySQL DROP INDEX 需使用 ON {TABLE_NAME} 语法
-- ==========================================================================
